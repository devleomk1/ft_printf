#include "ft_printf.h"

int	ft_printf(const char *s, ...)
{
	va_list ap;
	int printed;

	va_start(ap, s);
	printed = vsprintf(s, ap);
	va_end(ap);

	//puts(printf_buf);

	return (printed);
}
